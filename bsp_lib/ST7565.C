void St7565_WriteCmd(uint8_t com)    
{   
	uint8_t i,j;   
	lcd_cs_low();
	lcd_a0_low();
	lcd_sck_low();
	
	for(i=0;i<8;i++)   
	{    
		j=com;    
		lcd_sck_low();
		if( (com&0x80)==0x80)
		{
			lcd_sda_high();
		}
		else
		{
			lcd_sda_low();
		}
		lcd_sck_high();       
		com=j<<1;   
	}  
	lcd_cs_high();
	lcd_a0_high();   
}   
   
   
void St7565_WriteDat(uint8_t dat)    
{   
	uint8_t i,j; 
	lcd_cs_low();   //CS=0
	lcd_a0_high();	//A0=1
	lcd_sck_low();  
	for(i=0;i<8;i++)   
	{    
		j=dat;   
		lcd_sck_low();
		if( (dat&0x80)==0x80 )
		{
			lcd_sda_high();
		}
		else
		{
			lcd_sda_low();
		}
		lcd_sck_high();      
		dat=j<<1;   
	} 
  lcd_cs_high();
	lcd_a0_low(); 
} 

