void LCD_P8x16Str(unsigned char x,unsigned char y,unsigned char ch[])
{
	unsigned char c=0,i=0,j=0;
	while (ch[j]!='\0')
	{    
		c =ch[j]-32;
		if(x>0x7f)           
    {    
       x=0;   
       y++;   
		}
		Setadd(x,y*2);
		for(i=0;i<8;i++) 
		{
			St7565_WriteDat(SF8X16[c*16+i]);//Delay(10);
		}	     
		Setadd(x,y*2+1);
		for(i=0;i<8;i++)
		{
			St7565_WriteDat(SF8X16[c*16+i+8]);  //Delay(10);
		}
		x+=8;
		j++;
	}
	
}

